const langNames = { en: "EN", sv: "SV", no: "NO", de: "DE", fr: "FR", es: "ES" };

function applyLang(code) {
  const t = translations[code];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  localStorage.setItem('regma_lang', code);
  const btn = document.getElementById('lang-current');
  if (btn) btn.textContent = langNames[code];
  document.documentElement.lang = code;
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('regma_lang') || 'en';
  applyLang(saved);

  const toggle = document.getElementById('lang-toggle');
  const dropdown = document.getElementById('lang-dropdown');
  if (!toggle || !dropdown) return;

  toggle.addEventListener('click', (e) => {
    e.stopPropagation();
    const open = dropdown.classList.toggle('open');
    toggle.setAttribute('aria-expanded', open);
  });

  dropdown.querySelectorAll('[data-lang]').forEach(item => {
    item.addEventListener('click', () => {
      applyLang(item.dataset.lang);
      dropdown.classList.remove('open');
      toggle.setAttribute('aria-expanded', false);
    });
  });

  document.addEventListener('click', () => {
    dropdown.classList.remove('open');
    toggle.setAttribute('aria-expanded', false);
  });
});
